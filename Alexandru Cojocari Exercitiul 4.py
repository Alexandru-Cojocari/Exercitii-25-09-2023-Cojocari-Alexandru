# Varsta copilului de la tastatura
v = int(input("Introduce varsta copilului: "))

# Calculam greutatea si inaltimea ideala
greutate = 2 * v + 8  # Greutatea in kg
inaltime = 5 * v + 80  # Inaltimea in cm

# Rezultatele
print(f"Greutatea ideala pentru copilul de {v} ani este {greutate} kg.")
print(f"Inaltimea ideala pentru copilul de {v} ani este {inaltime} cm.")